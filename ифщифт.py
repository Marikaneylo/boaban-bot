def send_notifications_for_time(minutes_before):
    """–û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è –∑–∞ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–∏–Ω—É—Ç –¥–æ —Å–Ω–∞"""
    current_time = datetime.now()
    
    # –ò—â–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π, –∫–æ—Ç–æ—Ä—ã–º –Ω—É–∂–Ω–æ –æ—Ç–ø—Ä–∞–≤–∏—Ç—å —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ
    conn = sqlite3.connect('bot.db')
    cursor = conn.cursor()
    
    for hour in range(24):
        for minute in range(0, 60, 5):  # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–∞–∂–¥—ã–µ 5 –º–∏–Ω—É—Ç
            sleep_time = current_time.replace(hour=hour, minute=minute, second=0, microsecond=0)
            notification_time = sleep_time - timedelta(minutes=minutes_before)
            
            # –ï—Å–ª–∏ –≤—Ä–µ–º—è —É–≤–µ–¥–æ–º–ª–µ–Ω–∏—è —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å —Ç–µ–∫—É—â–∏–º (—Å —Ç–æ—á–Ω–æ—Å—Ç—å—é –¥–æ –º–∏–Ω—É—Ç—ã)
            if abs((notification_time - current_time).total_seconds()) < 60:
                cursor.execute('''
                    SELECT chat_id FROM users 
                    WHERE notifications_active = 1 
                    AND sleep_hour = ? AND sleep_minute = ?
                ''', (hour, minute))
                
                users = cursor.fetchall()
                for (chat_id,) in users:
                    message = random.choice(AGGRESSIVE_MESSAGES[minutes_before])
                    send_message(chat_id, message, get_sleep_button())
                    print(f"–û—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ —É–≤–µ–¥–æ–º–ª–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é {chat_id} –∑–∞ {minutes_before} –º–∏–Ω—É—Ç")
    
    conn.close()

